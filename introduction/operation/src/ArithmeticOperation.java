public class ArithmeticOperation {

    public static void main(String[] args) {
        int a = 45;
        int b = 160;

        int c = a + b;
        System.out.println("45 + 160 = " + c);
        c = a - b;
        System.out.println("45 - 160 = " + c);



    }
}
